export function generateMetadata({ params }: PageProps): Metadata {
  const meta = getDomainMeta(params.domain);
  if (!meta) return {};
  return {
    title: `${meta.name} 준비사항 안내 | 로앤가이드`,
    description: `${meta.name} 관련 문제가 있을 때 신고·상담 전에 필요한 준비사항을 안내합니다.`,
  };
}

